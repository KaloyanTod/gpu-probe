/// A usable, non-CPU GPU plus everything needed to describe it.
pub struct GpuContext {
    pub device: wgpu::Device,
    pub queue: wgpu::Queue,
    pub adapter_info: wgpu::AdapterInfo,
    /// Whether TIMESTAMP_QUERY was available and requested.
    pub timestamp_supported: bool,
}

pub struct BenchmarkResult {
    pub timing_method: String, // "timestamp_query" | "wall_clock"
    pub timestamp_period_ns: f64,
    pub raw_timings_ms: Vec<f64>,
    pub median_ms: f64,
    pub min_ms: f64,
    pub median_gflops: f64,
    pub min_gflops: f64,
    pub run_valid: bool,
    pub input_fingerprint: String,
    /// Inputs and GPU output kept for CPU verification.
    pub a: Vec<f32>,
    pub b: Vec<f32>,
    pub c: Vec<f32>,
}

/// Enumerate adapters, request a high-performance one, and REJECT a CPU/software
/// fallback adapter. A CPU pretending to be a GPU must never produce a score.
pub fn setup() -> Result<GpuContext, String> {
    let instance =
        wgpu::Instance::new(wgpu::InstanceDescriptor::new_without_display_handle_from_env());

    // Log what is available (useful when a machine reports "no_gpu").
    let all = pollster::block_on(instance.enumerate_adapters(wgpu::Backends::all()));
    eprintln!("Enumerated {} adapter(s):", all.len());
    for a in &all {
        let info = a.get_info();
        eprintln!(
            "  - {} [{:?}] backend={:?}",
            info.name, info.device_type, info.backend
        );
    }

    let adapter = pollster::block_on(instance.request_adapter(&wgpu::RequestAdapterOptions {
        power_preference: wgpu::PowerPreference::HighPerformance,
        compatible_surface: None,
        force_fallback_adapter: false,
        ..Default::default()
    }))
    .map_err(|e| format!("no_gpu: failed to acquire an adapter: {e}"))?;

    let info = adapter.get_info();

    // Hard reject: a CPU-fallback adapter is not a GPU.
    if info.device_type == wgpu::DeviceType::Cpu {
        return Err(format!(
            "no_gpu: only a CPU/software adapter is available ({}); refusing to score a CPU as a GPU.",
            info.name
        ));
    }

    // Detect timestamp-query support. We deliberately do NOT require
    // TIMESTAMP_QUERY_INSIDE_ENCODERS — we time around the compute pass using
    // ComputePassTimestampWrites (begin/end of pass), which only needs the base
    // TIMESTAMP_QUERY feature.
    let timestamp_supported = adapter
        .features()
        .contains(wgpu::Features::TIMESTAMP_QUERY);

    let required_features = if timestamp_supported {
        wgpu::Features::TIMESTAMP_QUERY
    } else {
        wgpu::Features::empty()
    };

    let (device, queue) = pollster::block_on(adapter.request_device(&wgpu::DeviceDescriptor {
        label: Some("gpu-probe device"),
        required_features,
        // Request exactly what the adapter supports so weak/integrated GPUs
        // don't fail the request.
        required_limits: adapter.limits(),
        memory_hints: wgpu::MemoryHints::Performance,
        trace: wgpu::Trace::Off,
        ..Default::default()
    }))
    .map_err(|e| format!("no_gpu: failed to create device: {e}"))?;

    Ok(GpuContext {
        device,
        queue,
        adapter_info: info,
        timestamp_supported,
    })
}

/// Static GPU resources reused across every iteration.
struct Pipeline {
    pipeline: wgpu::ComputePipeline,
    bind_group: wgpu::BindGroup,
    c_buffer: wgpu::Buffer,
    c_readback: wgpu::Buffer,
    c_size: u64,
    workgroups: u32,
}

fn build_pipeline(ctx: &GpuContext, cfg: &Config, a: &[f32], b: &[f32]) -> Pipeline {
    let device = &ctx.device;
    let n = cfg.n;
    let elems = (n as u64) * (n as u64);
    let byte_size = elems * std::mem::size_of::<f32>() as u64;

    let shader = device.create_shader_module(wgpu::ShaderModuleDescriptor {
        label: Some("gemm"),
        source: wgpu::ShaderSource::Wgsl(GEMM_WGSL.into()),
    });

    let a_buffer = device.create_buffer_init(&wgpu::util::BufferInitDescriptor {
        label: Some("A"),
        contents: bytemuck::cast_slice(a),
        usage: wgpu::BufferUsages::STORAGE,
    });
    let b_buffer = device.create_buffer_init(&wgpu::util::BufferInitDescriptor {
        label: Some("B"),
        contents: bytemuck::cast_slice(b),
        usage: wgpu::BufferUsages::STORAGE,
    });
    let c_buffer = device.create_buffer(&wgpu::BufferDescriptor {
        label: Some("C"),
        size: byte_size,
        usage: wgpu::BufferUsages::STORAGE | wgpu::BufferUsages::COPY_SRC,
        mapped_at_creation: false,
    });
    let c_readback = device.create_buffer(&wgpu::BufferDescriptor {
        label: Some("C readback"),
        size: byte_size,
        usage: wgpu::BufferUsages::COPY_DST | wgpu::BufferUsages::MAP_READ,
        mapped_at_creation: false,
    });

    // Uniform holding N. Uniform structs are 16-byte aligned, so we upload a
    // padded [n, 0, 0, 0].
    let dims: [u32; 4] = [n, 0, 0, 0];
    let dims_buffer = device.create_buffer_init(&wgpu::util::BufferInitDescriptor {
        label: Some("dims"),
        contents: bytemuck::cast_slice(&dims),
        usage: wgpu::BufferUsages::UNIFORM,
    });

    let pipeline = device.create_compute_pipeline(&wgpu::ComputePipelineDescriptor {
        label: Some("gemm pipeline"),
        layout: None,
        module: &shader,
        entry_point: Some("main"),
        compilation_options: wgpu::PipelineCompilationOptions::default(),
        cache: None,
    });

    let bind_group_layout = pipeline.get_bind_group_layout(0);
    let bind_group = device.create_bind_group(&wgpu::BindGroupDescriptor {
        label: Some("gemm bind group"),
        layout: &bind_group_layout,
        entries: &[
            wgpu::BindGroupEntry {
                binding: 0,
                resource: a_buffer.as_entire_binding(),
            },
            wgpu::BindGroupEntry {
                binding: 1,
                resource: b_buffer.as_entire_binding(),
            },
            wgpu::BindGroupEntry {
                binding: 2,
                resource: c_buffer.as_entire_binding(),
            },
            wgpu::BindGroupEntry {
                binding: 3,
                resource: dims_buffer.as_entire_binding(),
            },
        ],
    });

    Pipeline {
        pipeline,
        bind_group,
        c_buffer,
        c_readback,
        c_size: byte_size,
        workgroups: n / TILE_SIZE,
    }
}

/// Encode + submit one GEMM dispatch, optionally writing begin/end timestamps
/// into `qset`. Returns the submission index so the caller can wait on it.
fn dispatch_once(
    ctx: &GpuContext,
    pipe: &Pipeline,
    qset: Option<&wgpu::QuerySet>,
) -> wgpu::SubmissionIndex {
    let mut encoder = ctx
        .device
        .create_command_encoder(&wgpu::CommandEncoderDescriptor { label: None });

    let timestamp_writes = qset.map(|qs| wgpu::ComputePassTimestampWrites {
        query_set: qs,
        beginning_of_pass_write_index: Some(0),
        end_of_pass_write_index: Some(1),
    });

    {
        let mut cpass = encoder.begin_compute_pass(&wgpu::ComputePassDescriptor {
            label: Some("gemm pass"),
            timestamp_writes,
        });
        cpass.set_pipeline(&pipe.pipeline);
        cpass.set_bind_group(0, &pipe.bind_group, &[]);
        cpass.dispatch_workgroups(pipe.workgroups, pipe.workgroups, 1);
    }

    ctx.queue.submit(Some(encoder.finish()))
}

/// Block until the given submission completes.
fn wait(ctx: &GpuContext, index: wgpu::SubmissionIndex) {
    ctx.device
        .poll(wgpu::PollType::Wait {
            submission_index: Some(index),
            timeout: None,
        })
        .expect("device.poll failed");
}

/// Run warm-up then timed iterations and aggregate.
pub fn run(ctx: &GpuContext, cfg: &Config) -> BenchmarkResult {
    let (a, b) = generate_inputs(cfg.n, cfg.seed);
    let fingerprint = input_fingerprint(&a);
    let pipe = build_pipeline(ctx, cfg, &a, &b);

    // Warm-up: run untimed dispatches and force completion so clocks boost and
    // caches warm. A lazy queue could otherwise defer them, defeating warm-up.
    for _ in 0..cfg.warmup_iters {
        let idx = dispatch_once(ctx, &pipe, None);
        wait(ctx, idx);
    }

    let flops = 2.0 * (cfg.n as f64).powi(3);
    let gflops_from_ms = |ms: f64| flops / (ms * 1.0e6);

    let mut timings_ms: Vec<f64> = Vec::with_capacity(cfg.timed_iters as usize);
    let mut discarded = 0u32;

    let (timing_method, timestamp_period_ns) = if ctx.timestamp_supported {
        // ---- TIMESTAMP_QUERY path: time the pass on-device. ----
        let period_ns = ctx.queue.get_timestamp_period() as f64;

        let qset = ctx.device.create_query_set(&wgpu::QuerySetDescriptor {
            label: Some("timestamps"),
            ty: wgpu::QueryType::Timestamp,
            count: 2, // begin, end
        });
        // 2 * u64 ticks.
        let resolve_size = 2 * std::mem::size_of::<u64>() as u64;
        let resolve_buffer = ctx.device.create_buffer(&wgpu::BufferDescriptor {
            label: Some("query resolve"),
            size: resolve_size,
            usage: wgpu::BufferUsages::QUERY_RESOLVE | wgpu::BufferUsages::COPY_SRC,
            mapped_at_creation: false,
        });
        let ts_readback = ctx.device.create_buffer(&wgpu::BufferDescriptor {
            label: Some("query readback"),
            size: resolve_size,
            usage: wgpu::BufferUsages::COPY_DST | wgpu::BufferUsages::MAP_READ,
            mapped_at_creation: false,
        });

        for _ in 0..cfg.timed_iters {
            let mut encoder = ctx
                .device
                .create_command_encoder(&wgpu::CommandEncoderDescriptor { label: None });
            {
                let mut cpass = encoder.begin_compute_pass(&wgpu::ComputePassDescriptor {
                    label: Some("gemm pass (timed)"),
                    timestamp_writes: Some(wgpu::ComputePassTimestampWrites {
                        query_set: &qset,
                        beginning_of_pass_write_index: Some(0),
                        end_of_pass_write_index: Some(1),
                    }),
                });
                cpass.set_pipeline(&pipe.pipeline);
                cpass.set_bind_group(0, &pipe.bind_group, &[]);
                cpass.dispatch_workgroups(pipe.workgroups, pipe.workgroups, 1);
            }
            // Resolve the two timestamps and copy them somewhere mappable.
            encoder.resolve_query_set(&qset, 0..2, &resolve_buffer, 0);
            encoder.copy_buffer_to_buffer(&resolve_buffer, 0, &ts_readback, 0, resolve_size);
            let idx = ctx.queue.submit(Some(encoder.finish()));

            let ticks = map_read_u64(ctx, &ts_readback, idx);
            let (t0, t1) = (ticks[0], ticks[1]);
            let elapsed_ticks = t1.wrapping_sub(t0);
            let elapsed_ms = (elapsed_ticks as f64) * period_ns / 1_000_000.0;

            // Correctness guard: a non-positive elapsed means clock skew / tick
            // wrap. Discard rather than report a bogus fast score.
            if elapsed_ms <= 0.0 || t1 < t0 {
                discarded += 1;
                eprintln!("warning: discarded iteration (elapsed_ms={elapsed_ms})");
            } else {
                timings_ms.push(elapsed_ms);
            }
        }

        ("timestamp_query".to_string(), period_ns)
    } else {
        // ---- Wall-clock fallback: TIMESTAMP_QUERY absent. ----
        // Measure Instant around submit + poll(Wait). This includes a little
        // submit/sync overhead — acceptable, and exactly why timing_method is
        // recorded: wall_clock and timestamp_query numbers must never be pooled.
        for _ in 0..cfg.timed_iters {
            let start = Instant::now();
            let idx = dispatch_once(ctx, &pipe, None);
            wait(ctx, idx);
            let elapsed_ms = start.elapsed().as_secs_f64() * 1000.0;

            if elapsed_ms <= 0.0 {
                discarded += 1;
                eprintln!("warning: discarded iteration (elapsed_ms={elapsed_ms})");
            } else {
                timings_ms.push(elapsed_ms);
            }
        }

        ("wall_clock".to_string(), 0.0)
    };

    // Copy the final C back for verification (C holds the last dispatch result).
    let c = readback_c(ctx, &pipe);

    // Aggregate. If everything was discarded, produce a clearly-invalid row.
    let mut sorted = timings_ms.clone();
    sorted.sort_by(|x, y| x.partial_cmp(y).unwrap());

    let (median_ms, min_ms) = if sorted.is_empty() {
        (0.0, 0.0)
    } else {
        let mid = sorted.len() / 2;
        let median = if sorted.len() % 2 == 0 {
            (sorted[mid - 1] + sorted[mid]) / 2.0
        } else {
            sorted[mid]
        };
        (median, sorted[0])
    };

    let median_gflops = if median_ms > 0.0 {
        gflops_from_ms(median_ms)
    } else {
        0.0
    };
    let min_gflops = if min_ms > 0.0 {
        gflops_from_ms(min_ms)
    } else {
        0.0
    };

    // Validity: too many discards, nothing measured, or nonsense-fast score.
    let mut run_valid = true;
    if timings_ms.is_empty() {
        run_valid = false;
        eprintln!("warning: no valid timed iterations; marking run invalid.");
    }
    if discarded * 2 > cfg.timed_iters {
        run_valid = false;
        eprintln!(
            "warning: discarded {discarded}/{} iterations (>50%); marking run invalid.",
            cfg.timed_iters
        );
    }
    if min_gflops > SANITY_CEILING_GFLOPS {
        run_valid = false;
        eprintln!(
            "warning: min_gflops {min_gflops:.1} exceeds sanity ceiling {SANITY_CEILING_GFLOPS:.1}; marking run invalid."
        );
    }

    BenchmarkResult {
        timing_method,
        timestamp_period_ns,
        raw_timings_ms: timings_ms,
        median_ms,
        min_ms,
        median_gflops,
        min_gflops,
        run_valid,
        input_fingerprint: fingerprint,
        a,
        b,
        c,
    }
}

/// Map a small u64 buffer and read its contents, blocking until ready.
fn map_read_u64(ctx: &GpuContext, buffer: &wgpu::Buffer, idx: wgpu::SubmissionIndex) -> Vec<u64> {
    let slice = buffer.slice(..);
    let (tx, rx) = mpsc::channel();
    slice.map_async(wgpu::MapMode::Read, move |res| {
        let _ = tx.send(res);
    });
    wait(ctx, idx);
    rx.recv().unwrap().expect("map_async failed");

    let data = slice.get_mapped_range().expect("get_mapped_range failed");
    let values: Vec<u64> = bytemuck::cast_slice::<u8, u64>(&data).to_vec();
    drop(data);
    buffer.unmap();
    values
}

/// Copy C off the GPU and return it as f32 for verification.
fn readback_c(ctx: &GpuContext, pipe: &Pipeline) -> Vec<f32> {
    let mut encoder = ctx
        .device
        .create_command_encoder(&wgpu::CommandEncoderDescriptor { label: None });
    encoder.copy_buffer_to_buffer(&pipe.c_buffer, 0, &pipe.c_readback, 0, pipe.c_size);
    let idx = ctx.queue.submit(Some(encoder.finish()));

    let slice = pipe.c_readback.slice(..);
    let (tx, rx) = mpsc::channel();
    slice.map_async(wgpu::MapMode::Read, move |res| {
        let _ = tx.send(res);
    });
    wait(ctx, idx);
    rx.recv().unwrap().expect("map_async failed");

    let data = slice.get_mapped_range().expect("get_mapped_range failed");
    let values: Vec<f32> = bytemuck::cast_slice::<u8, f32>(&data).to_vec();
    drop(data);
    pipe.c_readback.unmap();
    values
}
