//! wgpu setup, deterministic input generation, tiled GEMM dispatch, and timing.
//!
//! Two subtle parts live here and are commented in place:
//!   1. Deterministic, platform-independent input generation (SplitMix64) so the
//!      same seed yields bit-identical matrices on every machine.
//!   2. The timestamp-query vs wall-clock timing branch.

use std::sync::mpsc;
use std::time::Instant;

use wgpu::util::DeviceExt;

use crate::Config;

/// The tiled GEMM shader, compiled into the binary. include_str! guarantees the
/// shader that RAN is exactly the text we hash — no drift between the shipped
/// file and the recorded shader_hash.
pub const GEMM_WGSL: &str = include_str!("shaders/gemm.wgsl");

/// Tile / workgroup dimension. MUST match `@workgroup_size` and `TILE` in the
/// shader.
pub const TILE_SIZE: u32 = 16;

/// Generous universal ceiling (GFLOP/s). Well above any current single consumer
/// GPU for f32. Exceeding it means the timing is broken (tick wrap, clock skew),
/// not that the GPU is that fast — we flag the run invalid rather than trust it.
pub const SANITY_CEILING_GFLOPS: f64 = 200_000.0;

// ---------------------------------------------------------------------------
// Deterministic PRNG (SplitMix64) — reproducibility depends on this being
// fully specified and identical on every platform.
//
// Determinism guarantees:
//   * Integer ops only inside the RNG (no floating point until to_unit_f32).
//   * Fixed iteration order: row-major, all of A then all of B, never
//     parallelized.
//   * to_unit_f32 uses only shifts/mul/sub — no platform math functions — so
//     its byte output is fixed forever.
// ---------------------------------------------------------------------------

/// SplitMix64. ~5 lines, deterministic, platform-independent.
#[inline]
fn splitmix64(state: &mut u64) -> u64 {
    *state = state.wrapping_add(0x9E3779B97F4A7C15);
    let mut z = *state;
    z = (z ^ (z >> 30)).wrapping_mul(0xBF58476D1CE4E5B9);
    z = (z ^ (z >> 27)).wrapping_mul(0x94D049BB133111EB);
    z ^ (z >> 31)
}

/// Map a u64 to an f32 in [-1, 1) using the top 24 bits. Signed values keep the
/// accumulation from only growing and exercise the hardware realistically. No
/// platform math functions — arithmetic only.
#[inline]
fn to_unit_f32(x: u64) -> f32 {
    let bits = (x >> 40) as u32; // top 24 bits
    let unit = (bits as f32) / (1u32 << 24) as f32; // [0, 1)
    unit * 2.0 - 1.0 // [-1, 1)
}

/// FNV-1a over raw bytes. Stable across Rust versions (unlike DefaultHasher),
/// so hashes recorded today stay meaningful forever.
pub fn fnv1a_64(bytes: &[u8]) -> u64 {
    let mut hash: u64 = 0xcbf29ce484222325;
    for &b in bytes {
        hash ^= b as u64;
        hash = hash.wrapping_mul(0x100000001b3);
    }
    hash
}

/// Generate A then B (each N*N f32, row-major, A fully before B) from `seed`.
pub fn generate_inputs(n: u32, seed: u64) -> (Vec<f32>, Vec<f32>) {
    let count = (n as usize) * (n as usize);
    let mut state = seed;
    let mut a = Vec::with_capacity(count);
    let mut b = Vec::with_capacity(count);
    for _ in 0..count {
        a.push(to_unit_f32(splitmix64(&mut state)));
    }
    for _ in 0..count {
        b.push(to_unit_f32(splitmix64(&mut state)));
    }
    (a, b)
}

/// Fingerprint of the first 256 generated bytes of A (or all of A if smaller).
/// Lets two machines PROVE they used identical inputs; differing fingerprints
/// mean the runs are not comparable.
pub fn input_fingerprint(a: &[f32]) -> String {
    let bytes: &[u8] = bytemuck::cast_slice(a);
    let take = bytes.len().min(256);
    format!("{:016x}", fnv1a_64(&bytes[..take]))
}

